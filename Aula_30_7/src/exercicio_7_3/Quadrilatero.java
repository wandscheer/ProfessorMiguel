package exercicio_7_3;

public class Quadrilatero {
	private int AX, AY, BX, BY, CX, CY, DX, DY;
	
	public Quadrilatero(){
		AX=0;
		AY=0;
		BX=0;
		BY=0;
		CX=0;
		CY=0;
		DX=0;
		DY=0;
	}
	
	public Quadrilatero(int AX, int AY, int BX, int BY, int CX, int CY, int DX, int DY){
		this.AX=AX;
		this.AY=AY;
		this.BX=BX;
		this.BY=BY;
		this.CX=CX;
		this.CY=CY;
		this.DX=DX;
		this.DY=DY;
	}
	
	public int getAX() {
		return AX;
	}

	public void setAX(int aX) {
		AX = aX;
	}

	public int getAY() {
		return AY;
	}

	public void setAY(int aY) {
		AY = aY;
	}

	public int getBX() {
		return BX;
	}

	public void setBX(int bX) {
		BX = bX;
	}

	public int getBY() {
		return BY;
	}

	public void setBY(int bY) {
		BY = bY;
	}

	public int getCX() {
		return CX;
	}

	public void setCX(int cX) {
		CX = cX;
	}

	public int getCY() {
		return CY;
	}

	public void setCY(int cY) {
		CY = cY;
	}

	public int getDX() {
		return DX;
	}

	public void setDX(int dX) {
		DX = dX;
	}

	public int getDY() {
		return DY;
	}

	public void setDY(int dY) {
		DY = dY;
	}
	
	
}
